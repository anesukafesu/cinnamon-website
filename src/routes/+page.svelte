  <p>
    When I was in high school, I wanted to build a programming language.
    I went on YouTube and followed some tutorials and nothing made sense. So I put the dream on hold.
  </p>
  <p>Later, in university, I enrolled in a Coursera course titled 'Nand2Tetris: Build a Modern Computer From
    First Principles' and I enjoyed it! It is one of the best Computer Science courses I have ever
    done. As part of the course, I had to build a compiler for a small, Java-inspired,
    programming language, Jack.
  </p>
  <p>
    That exercise gave me enough confidence and understanding to get started on my own programming language,
    which I named Cinnamon (<em>after the spice with one of my favourite smells</em>).
    After a few false starts, version 0.0.1 is here.  This version is still very limited, but I
    will add to it over time and build a good version 1.0.0. I do not expect this language to be useful to anyone, not even myself, apart
    from the learning experience. Nevertheless, I would love it if you tried it out for a moment.
  </p>
  <p>
    I have written a <a href="/tutorial">tutorial</a> for learning the language and created a
    <a href="/playground">playground</a>, where you can write and run Cinnamon programs in your browser.
    The grammar specification is on <a href="/grammar">this page</a>. I wrote the interpreter in TypeScript
    and the source code is hosted on <a href="https://github.com/anesukafesu/cinnamon">Github.</a> I have documented
    its internal implementation details there.
  </p>
